int	ft_sum(int val1, int val2)
{
	return (val1 + val2);
}

int	ft_subst(int val1, int val2)
{
	return (val1 - val2);
}

int	ft_mult(int val1, int val2)
{
	return (val1 * val2);
}

int	ft_div(int val1, int val2)
{
	return (val1 / val2);
}

int	ft_mod(int val1, int val2)
{
	return (val1 % val2);
}
