int		ft_str_is_numeric(char *str);

int	ft_str_is_numeric(char *str)
{
	int		i;

	i = 0;
	while (str && str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}
