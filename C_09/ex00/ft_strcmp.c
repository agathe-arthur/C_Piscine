int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		if (!(s1[i + 1]) && !(s2[i + 1]))
			return (0);
		i++;
	}
	if (s2[i])
		return (-(s2[i]));
	return (s1[i]);
}
